package org.achartengine.chartdemo.demo;

public class UserSetting {
	private String KEY_NAME; 
	private double KEY_TIME;
	private double KEY_TEMPETURE;
	private int KEY_SPEED;
	/**
	 * @return the kEY_NAME
	 */
	public String getKEY_NAME() {
		return KEY_NAME;
	}
	/**
	 * @param kEY_NAME the kEY_NAME to set
	 */
	public void setKEY_NAME(String kEY_NAME) {
		KEY_NAME = kEY_NAME;
	}
	/**
	 * @return the kEY_TIME
	 */
	public double getKEY_TIME() {
		return KEY_TIME;
	}
	/**
	 * @param kEY_TIME the kEY_TIME to set
	 */
	public void setKEY_TIME(double kEY_TIME) {
		KEY_TIME = kEY_TIME;
	}
	/**
	 * @return the kEY_TEMPETURE
	 */
	public double getKEY_TEMPETURE() {
		return KEY_TEMPETURE;
	}
	/**
	 * @param kEY_TEMPETURE the kEY_TEMPETURE to set
	 */
	public void setKEY_TEMPETURE(double kEY_TEMPETURE) {
		KEY_TEMPETURE = kEY_TEMPETURE;
	}
	/**
	 * @return the kEY_SPEED
	 */
	public int getKEY_SPEED() {
		return KEY_SPEED;
	}
	/**
	 * @param kEY_SPEED the kEY_SPEED to set
	 */
	public void setKEY_SPEED(int kEY_SPEED) {
		KEY_SPEED = kEY_SPEED;
	}
	
}
